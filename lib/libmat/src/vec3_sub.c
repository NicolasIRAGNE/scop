#include "libmat.h"

t_vec3      vec3_sub(t_vec3 u, t_vec3 v)
{
    t_vec3 ret;

    ret.x = u.x - v.x;
    ret.y = u.y - v.y;
    ret.z = u.z - v.z;
    return (ret);
}