#include "libmat.h"

t_mat3  mat3_mult(t_mat3 a, t_mat3 b)
{
    t_mat3 r;

    r = mat3_new();
    r[0] = a[0] * b[0] + a[1] * b[3] + a[3] * b[6];
    r[3] = a[0] * b[1] + a[1] * b[4] + a[3] * b[7];
    r[6] = a[0] * b[2] + a[1] * b[5] + a[3] * b[8];
    r[1] = a[3] * b[0] + a[4] * b[3] + a[5] * b[6];
    r[4] = a[3] * b[1] + a[4] * b[4] + a[5] * b[7];
    r[7] = a[3] * b[2] + a[4] * b[5] + a[5] * b[8];
    r[2] = a[6] * b[0] + a[7] * b[3] + a[8] * b[6];
    r[5] = a[6] * b[1] + a[7] * b[4] + a[8] * b[7];
    r[8] = a[6] * b[2] + a[7] * b[5] + a[8] * b[8];
    return (r);
}